def multiplicar(num1, num2):
    """"
    Funsion que multiplica dos numeros
    numero 1 (int)
    numero 2 (int)
    Retorna la multiplicacion de los parametros dados    
    
    >>> multiplicar(2,3)
    6
    >>> multiplicar(2,4)
    9
    """
    return num1 * num2
